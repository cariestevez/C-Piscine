#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	printnum;

	if (nb == -2147483648)
		write (1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			nb *= -1;
			write (1, "-", 1);
		}
		if (nb > 9)
			ft_putnbr(nb / 10);
		printnum = nb % 10 + 48;
		write(1, &printnum, 1);
	}
}
